/**
 * 从 Supabase DB 生成客户端数据文件
 * - person-registry.ts：人名注册表 + 标题→slug 映射
 *
 * 数据来源：
 *   1. 注册表：person_registry 表（get_person_registry RPC），回退 data/person-registry.json
 *   2. 标题映射：wiki_pages 表（get_all_wiki_pages RPC），回退扫描 data/wiki/
 */
const fs = require('fs')
const path = require('path')
const matter = require('gray-matter')

const registryPath = path.join(__dirname, '..', 'data', 'person-registry.json')
const contentsDir = path.join(__dirname, '..', 'data', 'wiki')
const outPath = path.join(__dirname, '..', 'src', 'data', 'person-registry.ts')

// 解析 [stu:xxx] 为纯文本缩写
function resolveInit(title, registry) {
  return title.replace(/\[(stu|usr|tch|per):([^\]]+)\]/g, (_m, _t, input) => {
    const all = [...registry.students, ...registry.teachers]
    const entry = all.find(e => e.initials === input.trim())
    return entry ? entry.initials : _m
  })
}

// 创建 Supabase 客户端
function makeSupabase() {
  require('dotenv').config({ path: path.join(__dirname, '..', '.env.local') })
  const { createClient } = require('@supabase/supabase-js')
  return createClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY,
  )
}

async function main() {
  let registry

  // ── 1. 加载注册表 ──
  try {
    const supabase = makeSupabase()
    const { data, error } = await supabase.rpc('get_person_registry')
    if (!error && data) {
      registry = data
    } else {
      throw error || new Error('empty data')
    }
  } catch (e) {
    console.warn('  ⚠ DB 注册表查询失败，回退文件:', e.message)
    registry = JSON.parse(fs.readFileSync(registryPath, 'utf-8'))
  }

  // ── 2. 构建标题→slug 映射 ──
  const titleSlugMap = {}

  try {
    const supabase = makeSupabase()
    const { data, error } = await supabase.rpc('get_all_wiki_pages')
    if (!error && data) {
      for (const row of data) {
        const title = (row.title || '').toString().trim()
        if (title) titleSlugMap[resolveInit(title, registry)] = row.slug
      }
    } else {
      throw error || new Error('empty data')
    }
  } catch (e) {
    console.warn('  ⚠ DB wiki 查询失败，回退文件系统:', e.message)
    // 文件系统扫描
    function scanMd(dir, segs) {
      let list
      try { list = fs.readdirSync(dir, { withFileTypes: true }) } catch { return }
      for (const e of list) {
        if (e.name.startsWith('_') || e.name.startsWith('.')) continue
        const full = path.join(dir, e.name)
        if (e.isDirectory()) { scanMd(full, [...segs, e.name]); continue }
        if (!e.name.endsWith('.md')) continue
        const id = [...segs, e.name.replace(/\.md$/, '')].join('/')
        if (id === 'home') continue
        try {
          const raw = fs.readFileSync(full, 'utf-8')
          const { data } = matter(raw)
          const title = (data.title || '').toString().trim()
          if (title) titleSlugMap[resolveInit(title, registry)] = id
        } catch {}
      }
    }
    scanMd(contentsDir, [])
  }

  // ── 3. 补充 oldToNewSlug ──
  const oldToNewSlug = {}
  if (registry.students) {
    for (const s of registry.students) {
      if (s.oldSlug && s.newSlug) oldToNewSlug[s.oldSlug] = s.newSlug
    }
  }
  registry.oldToNewSlug = oldToNewSlug

  // ── 4. 生成 TypeScript 文件 ──
  const content = `// Auto-generated by scripts/generate-person-ts.js
// Do not edit manually.

import type { PersonRegistry } from '@/lib/people'

export const registry: PersonRegistry = ${JSON.stringify(registry, null, 2)}

/** 标题→slug 映射，用于 [[WikiLink]] 解析 */
export const titleSlugMap: Record<string, string> = ${JSON.stringify(titleSlugMap, null, 2)}
`

  fs.mkdirSync(path.dirname(outPath), { recursive: true })
  fs.writeFileSync(outPath, content, 'utf-8')
  console.log(`✓ Generated src/data/person-registry.ts (${Object.keys(titleSlugMap).length} slugs)`)
}

main().catch(e => { console.error(e); process.exit(1) })
